package com.example.elton.rankingpopulacao;

public class Federacao{
    public String nome;
    public int populacao;
    public int ano;
    public int bandeira;

    public Federacao(String nome, int populacao, int ano, int bandeira){
        this.nome = nome;
        this.populacao = populacao;
        this.ano = ano;
        this.bandeira = bandeira;
    }

}